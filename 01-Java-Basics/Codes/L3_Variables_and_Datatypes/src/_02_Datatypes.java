public class _02_Datatypes {
    public static void main(String[] args) {

        boolean flag = true;

        byte a = 127;
        short b = 32767;
        int c = 999999999;
        long d = 999999999999999999L;

        float e = 880.7692F;
        double f = 7878.835354;

        char ch = 'p';

        System.out.println("The Boolean is " + flag);
        System.out.println("The Byte  is " + a);
        System.out.println("The Short  is " + b);
        System.out.println("The Int  is " + c);
        System.out.println("The Long  is " + d);
        System.out.println("The Float  is " + e);
        System.out.println("The Double  is " + f);
        System.out.println("The Char  is " + ch);


    }
}
