public class _02_If_Else_Statement {
    public static void main(String[] args) {
        /* If-Else Statement
        * Syntax:
        * if (Condition){
        *   //Code
        * }
        * else{
        *   //Code
        * }
        */

        // Example
        int num = 5;
        if (num % 2 == 0){
            System.out.println("Event");
        }
        else{
            System.out.println("Odd");
        }
    }

}
