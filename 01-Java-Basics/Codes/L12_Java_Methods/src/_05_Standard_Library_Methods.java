public class _05_Standard_Library_Methods {
    public static void main(String[] args) {
        /*Standard Library Methods
        * The standard library methods are built-in methods in Java that
        * are readily available for use. These standard libraries come
        * along with the Java Class Library (JCL) in a Java archive
        * (*.jar) file with JVM and JRE.*/

        //in-built Methods
        /*
        *print()
        *sqrt()
        *floor()
        * */

        System.out.println(Math.sqrt(24));
        System.out.println(Math.sqrt(16));

        System.out.println(Math.floor(5.3));
        System.out.println(Math.floor(5.8));

        System.out.println(Math.ceil(5.3));
        System.out.println(Math.ceil(5.8));

    }
}
