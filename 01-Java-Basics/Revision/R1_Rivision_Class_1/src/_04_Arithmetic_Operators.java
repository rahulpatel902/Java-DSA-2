public class _04_Arithmetic_Operators {
    public static void main(String[] args) {
        int x = 5;
        int y = 2;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);
    }
}
